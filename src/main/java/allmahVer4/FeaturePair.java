package allmahVer4;

public class FeaturePair {

		String name;
		String value;
		
		public FeaturePair(String n, String v) {
			name=n;
			value=v;
		}
	public String getName() {
		return name;
	}
	
	public String getValue() {
		return value;
	}
	 public void setName(String n) {
		 name=n;
	 }
	 
	 public void setValue(String v) {
		 value=v;
	 }

}
